package com.kuta.db.DAO.dbObjects;

/**
 * Medication
 */
public class Medication {

    private String id;
    private String name;
    private String shortDescription;
    private String type;
    public String getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getShortDescription() {
        return shortDescription;
    }
    public String getType() {
        return type;
    }

    
}
